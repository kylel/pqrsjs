Player = function (game, pos)
{
	Visible.call(this, game, false);
	this.game = game;
	this.game.add(this);
	this.setPosition(pos);

	this.weapon = new Gun(game, "launcher", this, 10, 500);
	this.weapon.load(10);
	this.game.add(this.weapon);
	this._keymap = {87 : 'up',
	                65 : 'left',
			  	    83 : 'down',
			 	    68 : 'right',
					82 : 'reload'};
	
	this._keys = {up:0,left:0,down:0,right:0};
	this._mouseState= 0;
	this.stepTime = .5;
	this.time = 0;
}

Player.prototype = new Visible();

Player.prototype.update = function (dt)
{
	if (this._keys.reload)
	{
		this.weapon.load(10);
		this._keys.reload = 0;
	}

	var vel = new Vector2D(this._keys.right - this._keys.left, this._keys.down  - this._keys.up);
	vel = vel.multiply(80);
	this.setVelocity(vel);
			
	if (this.vel.magnitude() > 0) this.feet.angle = Math.atan2(vel.x,vel.y);
		
	this.time += dt;

	if (this.time >= this.stepTime && this.vel.magnitude() > 0)
	{
		this.feet.scale(new Vector2D(1,-1));
		this.time = 0;
	}
	
	if (this._mouseState)
	{
		var mousePos = this.game.inputHandler.getMousePositionCanvas();
		mousePos = new Vector2D(mousePos.x,mousePos.y);
		mousePos = this.game.camera.cameraToWorld(mousePos);
		this.weapon.fire(mousePos);
	}
	
}

Player.prototype._render = function (ctx)
{
	this.feet.render(ctx, new Vector2D());
	draw_circle(ctx, 0, 0, 10, '#f00');
}

Player.prototype.changeKey = function(key, state)
{
	this._keys[this._keymap[key]] = state;
}

Player.prototype.click = function(x, y, state)
{
	this._mouseState = state;
}

Player.prototype.loadContent = function()
{
	this.feet = new Sprite(this.game.assetManager.getAsset("res/feet.png"));
	this.feet.setAnchor();
}

