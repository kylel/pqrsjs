Gun = function (game, name, player, clipSize, vel)
{
	this.game = game;
	this.player = player;
	this.pos = this.player.getPosition();
	this.name = name;
	this.weapon = null;
	this.ammo = 0;
	this.clipSize = clipSize;
	this.bulletVel = vel;
	this.rate = 2;
	this.timeSinceLastBullet = 0;
	Visible.call(this, game, false);
}

Gun.prototype = new Visible;

Gun.prototype.fire = function(target)
{
	if (this.ammo == 0) return;
	if (this.timeSinceLastBullet < 1/this.rate) return;

	this.ammo = this.ammo - 1;

	var direction = (target.subtract(this.pos)).unit();
	var bullet = new Bullet(this.game, this.pos, direction.multiply(this.bulletVel));
	this.game.add(bullet);
	this.timeSinceLastBullet = 0;
}

Gun.prototype.load = function(ammo)
{
	var rounds = ammo || this.clipSize;
	var empties = this.clipSize - this.ammo;
	if (rounds>=empties)
	{
		this.ammo = this.clipSize;
		return empties;
	}
	if (rounds<empties)
	{
		this.ammo += rounds;
		return rounds;
	}
}

Gun.prototype.update = function(dt)
{
	this.pos = this.player.getPosition();
	this.target = this.game.inputHandler.getMousePositionCanvas();
	this.target = this.game.camera.cameraToWorld(this.target);
	this.target = new Vector2D(this.target.x, this.target.y);
	this.target = this.target.subtract(this.pos);
	this.target = this.target.unit().multiply(10);
	this.timeSinceLastBullet += dt;
}

